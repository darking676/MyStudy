class Ex10{

  public static void main(String[] args){
	System.out.println("----------- JAVA RUN -----------");
	for(int i=1;i<=9;i++){
		for(int j=2;j<=9;j++){
			System.out.print(j+"*"+i+"="+i*j+"\t");
		}//for end
		System.out.print("\n");
	}//for end
	System.out.println("Normal Termination");
	System.out.println("출력 완료 (0초 경과).");
  }//main end

}//class end